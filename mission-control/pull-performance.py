import json, base64, urllib.request, os

token = open(os.path.expanduser(r"~/.dataforseo-auth")).read().strip()
domains = {
    "fgs":  "floridageotechnicalservices.com",
    "brm":  "blackrockmortgage.com",
    "te":   "tireexpressandservice.com",
    "jsc":  "jscfla.com",
}
def pull(target):
    task = [{"target": target, "location_code": 2840, "language_code": "en", "limit": 400}]
    req = urllib.request.Request(
        "https://api.dataforseo.com/v3/dataforseo_labs/google/ranked_keywords/live",
        data=json.dumps(task).encode(),
        headers={"Authorization": "Basic " + token, "Content-Type": "application/json"})
    r = json.load(urllib.request.urlopen(req))
    res = (r["tasks"][0].get("result") or [])
    return (res[0].get("items") if res else []) or []

out = {}
for sid, dom in domains.items():
    items = pull(dom)
    pages = {}
    fruit = []
    for it in items:
        kd = it.get("keyword_data", {}) or {}
        kw = kd.get("keyword", "")
        ki = (kd.get("keyword_info") or {})
        vol = ki.get("search_volume") or 0
        se = (it.get("ranked_serp_element") or {}).get("serp_item", {}) or {}
        url = se.get("url") or ""
        pos = se.get("rank_group") or se.get("rank_absolute") or 999
        etv = se.get("etv") or 0
        if url:
            p = pages.setdefault(url, {"url": url, "etv": 0, "kw": 0})
            p["etv"] += etv; p["kw"] += 1
        if 4 <= pos <= 15 and vol >= 40:
            fruit.append({"kw": kw, "pos": pos, "vol": vol, "url": url})
    top = sorted(pages.values(), key=lambda x: x["etv"], reverse=True)[:5]
    for p in top:
        p["etv"] = round(p["etv"])
        p["url"] = p["url"].replace("https://", "").replace("http://", "")
    fruit = sorted(fruit, key=lambda x: x["vol"], reverse=True)[:8]
    for f in fruit:
        f["url"] = f["url"].replace("https://", "").replace("http://", "")
    out[sid] = {"domain": domains[sid], "top": top, "fruit": fruit, "total_kw": len(items)}

js = "/* Auto-generated by _pull-performance.py — DataForSEO ranked keywords (US). Re-run monthly. */\n"
js += "window.MC_PERF = " + json.dumps(out, indent=2) + ";\n"
open(os.path.join(os.path.dirname(__file__), "data", "performance.js"), "w").write(js)
# console summary
for sid, d in out.items():
    print(f"\n== {sid} ({d['domain']}) — {d['total_kw']} kw")
    print("  TOP:", ", ".join(f"{p['url'][:40]} ({p['etv']})" for p in d["top"][:3]) or "none")
    print("  FRUIT:", ", ".join(f"{f['kw']}#{f['pos']}({f['vol']})" for f in d["fruit"][:4]) or "none")
print("\nperformance.js written")
